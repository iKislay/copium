def _generate_powershell() -> str:
    """Generate PowerShell completion script."""
    cmd_list = ", ".join(f"'{c}'" for c in COMMANDS)
    return f"""# Copium shell completion for PowerShell
# Generated by: copium completions powershell
# Install: copium completions powershell | Out-String | Invoke-Expression

Register-ArgumentCompleter -Native -CommandName 'copium' -ScriptBlock {{
    param($wordToComplete, $commandAst, $cursorPosition)

    $commands = @({cmd_list})

    $results = @()
    foreach ($cmd in $commands) {{
        if ($cmd -like "$wordToComplete*") {{
            $results += [System.Management.Automation.CompletionResult]::new(
                $cmd,
                $cmd,
                'ParameterValue',
                $cmd
            )
        }}
    }}

    $results
}}
"""


@click.command()
@click.argument("shell", type=click.Choice(["bash", "zsh", "fish", "powershell"]))
def completions(shell: str) -> None:
    """Generate shell completion scripts.

    Outputs a completion script to stdout. Redirect to the appropriate
    file to install.

    \b
    Examples:
        copium completions bash >> ~/.bash_completion
        copium completions zsh >> ~/.zshrc
        copium completions fish > ~/.config/fish/completions/copium.fish
        copium completions powershell | Out-String | Invoke-Expression

    \b
    After installing, reload your shell or run:
        source ~/.bash_completion   # bash
        source ~/.zshrc             # zsh
        exec fish                   # fish
    """
    generators = {
        "bash": _generate_bash,
        "zsh": _generate_zsh,
        "fish": _generate_fish,
        "powershell": _generate_powershell,
    }

    script = generators[shell]()
    click.echo(script)

    # Show installation hint
    hints = {
        "bash": "copium completions bash >> ~/.bash_completion",
        "zsh": "copium completions zsh >> ~/.zshrc",
        "fish": "copium completions fish > ~/.config/fish/completions/copium.fish",
        "powershell": "copium completions powershell | Out-String | Invoke-Expression",
    }

    console.print(
        f"\n[dim]Install with: [bold]{hints[shell]}[/bold][/dim]"
    )
