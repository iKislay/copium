def _generate_bash() -> str:
    """Generate bash completion script."""
    cmd_list = " ".join(COMMANDS)
    return f"""#!/usr/bin/env bash
# Copium shell completion for bash
# Generated by: copium completions bash
# Install: copium completions bash >> ~/.bash_completion

_copium_completions() {{
    local cur prev commands
    COMPREPLY=()
    cur="${{COMP_WORDS[COMP_CWORD]}}"
    prev="${{COMP_WORDS[COMP_CWORD-1]}}"
    commands="{cmd_list}"

    # Global flags
    if [[ "$cur" == -* ]]; then
        COMPREPLY=( $(compgen -W "--help --version --json --quiet --verbose" -- "$cur") )
        return 0
    fi

    # Subcommand completion
    if [[ $COMP_CWORD -eq 1 ]]; then
        COMPREPLY=( $(compgen -W "$commands" -- "$cur") )
        return 0
    fi

    # Group subcommands
    local group="${{COMP_WORDS[1]}}"
    case "$group" in
        config)
            COMPREPLY=( $(compgen -W "path reset set" -- "$cur") )
            ;;
        init)
            COMPREPLY=( $(compgen -W "aider claude codex copilot cursor hook openclaw opencode" -- "$cur") )
            ;;
        memory)
            COMPREPLY=( $(compgen -W "delete edit export import list prune purge show stats" -- "$cur") )
            ;;
        service)
            COMPREPLY=( $(compgen -W "install logs remove status" -- "$cur") )
            ;;
        wrap)
            COMPREPLY=( $(compgen -W "aider claude codex continue copilot cursor openclaw opencode vibe" -- "$cur") )
            ;;
    esac

    return 0
}}

complete -F _copium_completions copium
"""


